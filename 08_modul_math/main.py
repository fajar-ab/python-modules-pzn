import math

# operasi akar dan pangkat
print(f"akar kuadrat 16 : {math.sqrt(16)}")
print(f"akar kubik 8 : {math.pow(8, 1/3)}")
print(f"2 pangkat 10 : {math.pow(2, 10)}")

# pembulatan pembagian
print(f"ceiling 4.2 : {math.ceil(4.2)}")
print(f"floor 4.8 : {math.floor(4.8)}")

# logaritma
print(f"log natural 10 : {math.log(10)}")
print(f"log basis 10 : {math.log10(100)}" )

print(f"nilai pi : {math.pi}")