import math

def luas_persegi(sisi):
    """menghitung luas persegi"""
    return sisi * sisi

def luas_persegi_panjang(panajang, lebar):
    """menghitung luas persegi panjang"""
    return panjang * lebar

def luas_lingkaran(radius):
    """menghitung luas persegi panjang"""
    return math.pi * (radius ** 2)